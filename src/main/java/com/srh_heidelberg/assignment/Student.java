package com.srh_heidelberg.assignment;

public class Student {

	public String firstName;
	public String lastName;
	public String gender;
	public boolean maritalStatus;
	public double height;
	public String eyeColor;
	public String hairColor;
	public String primarySchool;
	public String secondarySchool;
	public String university;
	public String localAddress;
	public long phoneNumber;
	
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public boolean isMaritalStatus() {
		return maritalStatus;
	}
	public void setMaritalStatus(boolean maritalStatus) {
		this.maritalStatus = maritalStatus;
	}
	public String getEyeColor() {
		return eyeColor;
	}
	public void setEyeColor(String eyeColor) {
		this.eyeColor = eyeColor;
	}
	public String getHairColor() {
		return hairColor;
	}
	public void setHairColor(String hairColor) {
		this.hairColor = hairColor;
	}
	public String getPrimarySchool() {
		return primarySchool;
	}
	public void setPrimarySchool(String primarySchool) {
		this.primarySchool = primarySchool;
	}
	public String getSecondarySchool() {
		return secondarySchool;
	}
	public void setSecondarySchool(String secondarySchool) {
		this.secondarySchool = secondarySchool;
	}
	public String getUniversity() {
		return university;
	}
	public void setUniversity(String university) {
		this.university = university;
	}
	public String getLocalAddress() {
		return localAddress;
	}
	public void setLocalAddress(String localAddress) {
		this.localAddress = localAddress;
	}
	public long getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(long phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public double getHeight() {
		return height;
	}
	public void setHeight(double height) {
		this.height = height;
	}

	
}
